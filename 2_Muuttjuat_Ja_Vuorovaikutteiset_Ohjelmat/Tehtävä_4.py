luku1 = float(input("Syötä ensimmäinen luku: "))
luku2 = float(input("Syötä toinen luku: "))
luku3 = float(input("Syötä kolmas luku: "))

print(f"{'Summa':10s}:{luku1 + luku2 + luku3:8.2f}")
print(f"{'Tulo':10s}:{luku1 * luku2 * luku3:8.2f}")
print(f"{'Keskiarvo':10s}:{(luku1 * luku2 * luku3)/3:8.2f}")
